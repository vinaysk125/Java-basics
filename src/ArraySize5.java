public class ArraySize5 {
    public static void main(String[] args){
        System.out.println("This is ArraySize5 program.");

        int[] number = {1, 2, 3, 4, 5};

        for (int i = 0; i < number.length; i++){
            System.out.println("Value = " + i + " is " + number[i]);
        }
    }

}
